ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    priority_queue<ll> l;
    priority_queue<ll, vector<ll>, greater<ll>> r;

    ll x;
    cin >> x;

    l.push(x);
    cin >> n;
    while (n--)
    {
        ll v;

        for (int i = 1; i <= 2; i++)
        {
            cin >> v;
            if (v <= l.top())
            {
                l.push(v);
            }
            else
            {
                r.push(v);
            }
            while (l.size() < r.size())
            {
                l.push(r.top());
                r.pop();
            }

            while (l.size() > r.size() + 1)
            {
                r.push(l.top());
                l.pop();
            }
        }

        cout << l.top() << '\n';
    }
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    // cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}