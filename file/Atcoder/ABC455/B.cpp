#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define ld long double
#define ull unsigned long long
#define INF 0x3f3f3f3f
#define PII pair<ll, ll>
const ll mod = 1e9 + 7;
const ll MAXN = 500005;
const ll base1 = 131;
const ll base2 = 127;
ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    char a[100][100];
    cin >> n >> m;
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            cin >> a[i][j];
        }
    }
    for (int h1 = 1; h1 <= n; h1++)
    {
        for (int h2 = h1; h2 <= n; h2++)
        {
            for (int w1 = 1; w1 <= m; w1++)
            {
                for (int w2 = w1; w2 <= m; w2++)
                {
                    bool flag = 1;
                    for (int i = h1; i <= h2; i++)
                    {
                        for (int j = w1; j <= w2; j++)
                        {
                            if (a[i][j] != a[h1 + h2 - i][w1 + w2 - j])
                            {
                                flag = 0;
                            }
                        }
                    }
                    ans += flag;
                }
            }
        }
    }
    cout << ans << '\n';
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    // cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}