struct node
{
    ll c, sum;
};
void solve()
{
    ll k;
    cin >> n >> k;
    map<ll, ll> ma;
    ll x;
    for (int i = 1; i <= n; i++)
    {
        cin >> x;
        ma[x]++;
    }
    vector<ll> ve;
    ll sum = 0;
    for (auto it : ma)
    {
        sum += it.first * it.second;
        ve.push_back(it.first * it.second);
    }
    sort(ve.begin(), ve.end(), greater<ll>());
    for (int i = 0; i < min(k, (ll)ve.size()); i++)
    {
        sum -= ve[i];
    }
    cout << sum << '\n';
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    // cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}