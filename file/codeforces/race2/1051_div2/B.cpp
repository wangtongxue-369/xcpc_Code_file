const ll MAXN = 500005;
const ll base1 = 131;
const ll base2 = 127;
ll _ = 1, n, m, ans = 0, a[MAXN], b[MAXN];
void solve()
{
    ll k;
    cin >> n >> k;
    ans = 0;
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
        ans += a[i];
    }
    for (int i = 1; i <= k; i++)
    {
        cin >> b[i];
    }
    sort(a + 1, a + 1 + n, greater<ll>());
    sort(b + 1, b + 1 + k);

    ll p = 1;
    for (int i = 1; i <= k; i++)
    {
        p += (b[i] - 1);
        if (p <= n)
        {
            ans -= a[p];
        }
        p++;
    }
    cout << ans << '\n';
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}