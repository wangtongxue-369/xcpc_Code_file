#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define ull unsigned long long
#define INF 0x3f3f3f3f
#define PII pair<ll, ll>
const ll mod = 1e9 + 7;
const ll MAXN = 500005;
const ll base1 = 131;
const ll base2 = 127;
ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    ll c;
    cin >> n >> c;
    vector<ll> ve;
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
        if (a[i] <= c)
        {
            ve.push_back(a[i]);
        }
    }
    ans = n - ve.size();
    ll cs = 0;
    while (ve.size())
    {
        sort(ve.begin(), ve.end());
        while (ve.size() && (ve.back()) > c)
        {
            ans++;
            ve.pop_back();
        }
        if (ve.size())
        {
            ve.pop_back();
        }
        for (auto &it : ve)
        {
            it *= 2;
        }
    }
    cout << ans << '\n';
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}