void solve()
{
    ll a, b, k;
    cin >> a >> b >> k;
    ll c = __gcd(a, b);
    if (a / c <= k && b / c <= k)
    {
        cout << 1 << '\n';
        return;
    }
    cout << 2 << '\n';
}
