void solve()
{
    int64_t n, q;
    std::cin >> n >> q;
    std::vector<int64_t> a(n + 10);
    for (int i = 1; i <= n; i++)
    {
        std::cin >> a[i];
        a[i] += a[i - 1];
    }
    while (q--)
    {
        int64_t t;
        std::cin >> t;
        t;
        // 1 2 4 7
        //
        int p = upper_bound(a.begin() + 1, a.begin() + 1 + n, t) - a.begin();
        std::cout << p << '\n';
    }
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    // std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}