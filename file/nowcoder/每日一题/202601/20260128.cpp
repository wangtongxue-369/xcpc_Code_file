#include <bits/stdc++.h>

void solve()
{

    int64_t n;
    std::cin >> n;
    int64_t ans = 0;
    if (n % 4 == 0)
    {
        ans = n;
    }
    else if (n % 4 == 1)
    {
        ans = 0;
    }
    else if (n % 4 == 2)
    {
        ans = n + 1;
    }
    else
    {
        n = 1;
    }
    std::cout << ans << '\n';
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}