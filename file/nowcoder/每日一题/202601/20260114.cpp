#include <bits/stdc++.h>
void solve()
{
    int64_t n, a1, m;
    std::cin >> n >> a1 >> m;
    std::vector<int64_t> a(n + 10);
    a[1] = a1;
    for (int i = 2; i <= n; i++)
    {
        a[i] = (a[i - 1] + 7 * i) % m;
    }
    std::vector<int64_t> b(n + 10);
    for (int i = 1; i <= n; i++)
    {
        for (int j = i; j <= n; j += i)
        {
            b[j] += a[i];
        }
    }
    int64_t ans = 0;
    for (int i = 1; i <= n; i++)
    {
        ans ^= b[i];
    }
    std::cout << ans << '\n';
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    // std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}