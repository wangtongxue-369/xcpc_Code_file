#include <bits/stdc++.h>
void solve()
{
    int64_t n, m;
    std::cin >> n >> m;
    int64_t x = n ^ m, ans = 0;
    for (int i = 0; i <= 31; i++)
    {
        if (x & (1ll << i))
        {
            ans++;
        }
    }
    std::cout << ans << '\n';
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    // std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}