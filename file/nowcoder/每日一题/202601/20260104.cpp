#include <bits/stdc++.h>
void solve()
{
    int64_t n;
    std::cin >> n;
    if ((n / 2) % 2 == 1)
    {
        std::cout << "NO\n";
        return;
    }
    std::cout << "YES\n";
    int64_t sum = 0;
    for (int64_t i = 1; i <= n / 2; i++)
    {
        sum += i * 2;
        std::cout << i * 2 << ' ';
    }
    for (int64_t i = 1; i < n / 2; i++)
    {
        sum -= i * 2 - 1;
        std::cout << i * 2 - 1 << ' ';
    }
    std::cout << sum;
    std::cout << '\n';
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}