#include <bits/stdc++.h>
void solve()
{
    std::string s;
    std::cin >> s;
    int64_t c0 = 0, c1 = 0;
    for (auto it : s)
    {
        if (it == '0')
        {
            c0++;
        }
        else
        {
            c1++;
        }
    }
    if (c1 % 2 == 0 || c0 % 2 == 0)
    {
        std::cout << "Yes\n";
    }
    else
    {
        std::cout << "No\n";
    }
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}