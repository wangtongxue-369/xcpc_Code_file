#include <bits/stdc++.h>
void solve()
{
    int64_t x;
    std::cin >> x;
    if (x >= 15)
    {
        std::cout << "Yes\n";
        return;
    }

    if (x == 1 || x == 4 || x == 7 || x == 9 || x == 10 || x == 12 || x == 13)
    {
        std::cout << "Yes\n";
        return;
    }
    std::cout << "No\n";
}
