#include <bits/stdc++.h>
void solve()
{
    int x, y;
    std::cin >> x >> y;
    if (x == y)
    {
        std::cout << "0\n";
    }
    else if (y == 0)
    {
        std::cout << "1\n";
    }
    else if (x == 0)
    {
        std::cout << "2\n";
    }
    else if (x + y == 0)
    {
        std::cout << "3\n";
    }
    else
    {
        std::cout << "-1\n";
    }
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    // std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}