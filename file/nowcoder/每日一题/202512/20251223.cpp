#include <bits/stdc++.h>
void solve()
{
    int n;
    std::cin >> n;
    std::vector<int> a(n);
    int ans = 0;
    for (auto &it : a)
    {
        std::cin >> it;
        ans ^= it;
    }
    std::cout << ans << '\n';
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}