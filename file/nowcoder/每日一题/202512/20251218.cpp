#include <bits/stdc++.h>
void solve()
{
    int64_t a, b, x;
    std::cin >> a >> b >> x;
    if (a < x / 2 || b < x / 2)
    {
        std::cout << -1 << '\n';
        return;
    }
    if ((a - x / 2) % 2 || (b - x / 2) % 2)
    {
        std::cout << -1 << '\n';
        return;
    }
    // std::cerr << "!!!\n";
    std::string ans;
    for (int i = 1; i <= a - x / 2; i++)
    {
        ans += '0';
    }
    for (int i = 1; i <= x / 2; i++)
    {
        ans += '0';
        ans += '1';
    }
    for (int i = 1; i <= b - x / 2; i++)
    {
        ans += '1';
    }

    std::cout << ans << '\n';
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    // std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}