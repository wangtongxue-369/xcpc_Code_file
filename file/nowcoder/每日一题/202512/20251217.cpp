#include <bits/stdc++.h>
void solve()
{
    int64_t l, r, x, sum, q;
    std::cin >> l >> r;
    sum = (r - l + 1) * (l + r) / 2;
    std::cin >> q;
    while (q--)
    {
        std::cin >> x;
        if (sum % x == 0)
        {
            std::cout << 0 << '\n';
        }
        else
        {
            std::cout << 1 << '\n';
        }
    }
}
signed main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(0);
    int _ = 1;
    std::cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}