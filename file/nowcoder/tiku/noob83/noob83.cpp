ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    cin >> n;
    stack<ll> st;
    for (int i = 1; i <= n; i++)
    {
        string s;
        cin >> s;
        ll x;
        if (s == "push")
        {
            cin >> x;
            st.push(x);
        }
        else if (s == "query")
        {
            if (st.size())
            {
                cout << st.top() << '\n';
            }
            else
            {
                cout << "Empty\n";
            }
        }
        else if (s == "pop")
        {
            if (st.size())
            {
                st.pop();
            }
            else
            {
                cout << "Empty\n";
            }
        }
        else if (s == "size")
        {
            cout << st.size() << '\n';
        }
    }
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    // cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}