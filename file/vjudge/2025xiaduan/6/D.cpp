void solve()
{
    cin >> n;
    string s;
    cin >> s;
    ans = 1e18;
    ll sum = 0;
    ll nx = 0;
    for (int i = 0; i < s.length(); i++)
    {
        if (s[i] == 'A')
        {
            sum += abs(i - nx);
            nx += 2;
        }
    }
    ans = min(ans, sum);
    sum = 0;
    nx = 1;
    for (int i = 0; i < s.length(); i++)
    {
        if (s[i] == 'A')
        {
            sum += abs(i - nx);
            nx += 2;
        }
        // cout << sum << '\n';
    }
    ans = min(ans, sum);
    cout << ans << '\n';
}
