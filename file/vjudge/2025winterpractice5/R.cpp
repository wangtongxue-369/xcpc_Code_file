#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define ull unsigned long long
#define INF 0x3f3f3f3f
#define PII pair<ll, ll>
const ll mod = 1e9 + 7;
const ll MAXN = 500005;
const ll base1 = 131;
const ll base2 = 127;
ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    cin >> n >> m;
    function<void(ll i, ll x)> dfs = [&](ll i, ll x)
    {
        if (i > n && x <= m)
        {
            return;
        }
        if (x > m)
        {
            for (int j = 1; j <= m; j++)
            {
                cout << a[j] << ' ';
            }
            cout << '\n';
            return;
        }
        for (i; i <= n; i++)
        {
            a[x] = i;
            dfs(i + 1, x + 1);
        }
    };
    dfs(1, 1);
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    // cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}