ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    cin >> n;
    string s;
    cin >> s;
    for (int i = 1; i <= n; i++)
    {
        cin >> a[i];
    }
    if (n == 2)
    {
        cout << "Alice\n";
        return;
    }
    ll sum = 0;
    for (int i = 1; i <= n; i++)
    {
        if (i != a[i])
        {
            sum++;
        }
    }
    if (s == "Alice")
    {
        if (sum == 2)
        {
            cout << "Alice\n";
            return;
        }
        else
        {
            cout << "Bob\n";
            return;
        }
    }
    else
    {
        if (n == 3 && sum == 3)
        {
            cout << "Alice\n";
            return;
        }
        cout << "Bob\n";
    }
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}