#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define ull unsigned long long
#define INF 0x3f3f3f3f
#define PII pair<ll, ll>
const ll mod = 1e9 + 7;
const ll MAXN = 500005;
const ll base1 = 131;
const ll base2 = 127;
ll _ = 1, n, m, ans = 0; // a[MAXN], f[MAXN];

void solve()
{
    ll a, b;
    cin >> a >> b;

    map<PII, ll> ma;
    function<void(ll x1, ll x2, ll cnt)> fin = [&](ll x1, ll x2, ll cnt)
    {
        if (x1 == 0 || x2 == 0)
        {
            ans = min(ans, cnt + 1);
            return;
        }
        if (cnt > ans)
        {
            return;
        }
        ll t = __gcd(x1, x2);
        fin(x1 - t, x2, cnt + 1);
        fin(x1, x2 - t, cnt + 1);
    };
    ans = a + 1;
    fin(a, b, 0);
    cout << ans << '\n';
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}