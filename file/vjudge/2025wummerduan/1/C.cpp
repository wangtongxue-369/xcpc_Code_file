#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define ull unsigned long long
#define INF 0x3f3f3f3f
#define PII pair<ll, ll>
const ll mod = 1e9 + 7;
const ll MAXN = 500005;
const ll base1 = 131;
const ll base2 = 127;
ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    ll k;
    cin >> n >> k;
    ll cs = 0;
    m = n;
    while (m)
    {
        ll x = (m + k - 1) / k;
        ll cnt = (m - k * (x - 1) + (x - 1)) / x;
        cs += cnt;
        m -= x * cnt;
    }
    ans = 1;
    cs--;
    while (cs)
    {
        ll p = (ans + k - 2) / (k - 1);
        ll jg = ((p) * (k - 1) - ans) / p + 1;
        if (jg > cs)
        {
            ans += cs * p;
            break;
        }
        ans += jg * p;
        cs -= jg;
    }
    cout << ans << '\n';
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}