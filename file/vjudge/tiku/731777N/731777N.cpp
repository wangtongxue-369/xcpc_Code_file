#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define ull unsigned long long
#define INF 0x3f3f3f3f
#define PII pair<ll, ll>
const ll mod = 1e9 + 7;
const ll MAXN = 500005;
const ll base1 = 131;
const ll base2 = 127;
ll _ = 1, n, m, ans = 0, a[MAXN], f[MAXN];
void solve()
{
    ll u, v;
    cin >> n;
    vector<vector<ll>> ve(n + 10);
    for (int i = 1; i < n; i++)
    {
        cin >> u >> v;
        ve[u].push_back(v);
        ve[v].push_back(u);
    }
    vector<ll> dep(n + 10, 0);
    ll t1 = 1;
    function<void(ll x, ll fa, ll t)> dfs = [&](ll x, ll fa, ll t)
    {
        dep[x] = t;
        if (dep[x] > dep[t1])
        {
            t1 = x;
        }
        for (auto it : ve[x])
        {
            if (it == fa)
            {
                continue;
            }
            dfs(it, x, t + 1);
        }
    };
    dfs(1, 0, 1);
    dfs(t1, 0, 1);
    cout << dep[t1] - 1 << '\n';
}
signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    // cin >> _;
    while (_--)
    {
        solve();
    }
    return 0;
}